/**
 * Update the denormalised userName / userUsername across all rankings owned by
 * a user. Mirrors updateRankingsUsername() in the old Firestore module.
 * SQLite has no batch-write limit so a single transaction suffices.
 */
export async function updateRankingsUsername(
    userId: string,
    newDisplayName: string,
    newUsername?: string
): Promise<void> {
    const patch: Partial<typeof rankings.$inferInsert> = {
        userName: newDisplayName,
        updatedAt: Date.now(),
    }
    if (newUsername !== undefined) {
        patch.userUsername = newUsername
    }

    await db.update(rankings).set(patch).where(eq(rankings.userId, userId))
}
