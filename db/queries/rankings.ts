/**
 * Rankings Drizzle query module.
 *
 * Replaces utils/firestore/rankings.ts. All Firestore primitives are mapped:
 *   - increment()         → sql`${col} + 1` / sql`${col} - 1`
 *   - runTransaction()    → db.transaction()
 *   - writeBatch()        → db.transaction() (batch updates in a single tx)
 *   - arrays / objects    → JSON columns (Drizzle handles serialisation)
 *
 * Ownership is always derived from the Auth.js session by the API route layer
 * (via withAuth); the functions here trust the userId passed by the route.
 */

import { and, desc, eq, inArray, sql } from 'drizzle-orm'

import { db } from '@/db'
import { rankingLikes, rankings } from '@/db/schema'
import type {
    CreateRankingRequest,
    RankedItem,
    Ranking,
    UpdateRankingRequest,
} from '@/types/rankings'

