/**
 * Increment the view counter for a ranking.
 *
 * Mirrors incrementRankingView() in the old Firestore module.
 * If a viewerId is supplied the function skips incrementing when the viewer is
 * the ranking owner (same behaviour as before).
 */
export async function incrementRankingView(
    rankingId: string,
    viewerId?: string | null
): Promise<void> {
    // When viewerId is supplied, skip if the viewer is the owner
    if (viewerId) {
        const row = await db
            .select({ userId: rankings.userId })
            .from(rankings)
            .where(eq(rankings.id, rankingId))
            .limit(1)

        if (row.length > 0 && row[0].userId === viewerId) {
            return // Owner viewing their own ranking — don't count
        }
    }

    await db
        .update(rankings)
        .set({ views: sql`${rankings.views} + 1` })
        .where(eq(rankings.id, rankingId))
}

