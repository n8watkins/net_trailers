/**
 * Like a ranking.
 *
 * Uses a transaction that checks for an existing like record, inserts one if
 * absent, and increments the denormalised likes counter atomically.
 * Returns true when the like was newly created, false if already liked.
 */
export async function likeRanking(userId: string, rankingId: string): Promise<boolean> {
    return await db.transaction(async (tx) => {
        // Check for existing like
        const existing = await tx
            .select({ id: rankingLikes.id })
            .from(rankingLikes)
            .where(and(eq(rankingLikes.userId, userId), eq(rankingLikes.rankingId, rankingId)))
            .limit(1)

        if (existing.length > 0) {
            return false // Already liked
        }

        // Insert like record
        await tx.insert(rankingLikes).values({
            rankingId,
            userId,
            createdAt: Date.now(),
        })

        // Increment counter — sql`` prevents N+1 read-modify-write race
        await tx
            .update(rankings)
            .set({ likes: sql`${rankings.likes} + 1` })
            .where(eq(rankings.id, rankingId))

        return true
    })
}

/**
 * Unlike a ranking.
 *
 * Deletes the like record and decrements the counter (floor 0).
 * Returns true when the unlike was applied, false if no like existed.
 */
export async function unlikeRanking(userId: string, rankingId: string): Promise<boolean> {
    return await db.transaction(async (tx) => {
        const existing = await tx
            .select({ id: rankingLikes.id })
            .from(rankingLikes)
            .where(and(eq(rankingLikes.userId, userId), eq(rankingLikes.rankingId, rankingId)))
            .limit(1)

        if (existing.length === 0) {
            return false // Not liked
        }

        await tx
            .delete(rankingLikes)
            .where(and(eq(rankingLikes.userId, userId), eq(rankingLikes.rankingId, rankingId)))

        await tx
            .update(rankings)
            .set({ likes: sql`MAX(0, ${rankings.likes} - 1)` })
            .where(eq(rankings.id, rankingId))

        return true
    })
}

/** Check whether a user has liked a ranking. */
export async function hasUserLikedRanking(userId: string, rankingId: string): Promise<boolean> {
    const rows = await db
        .select({ id: rankingLikes.id })
        .from(rankingLikes)
        .where(and(eq(rankingLikes.userId, userId), eq(rankingLikes.rankingId, rankingId)))
        .limit(1)

    return rows.length > 0
}

