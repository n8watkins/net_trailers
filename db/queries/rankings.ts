/**
 * Extract denormalised content metadata from ranked items.
 * Mirrors extractContentMetadata() in the old Firestore module.
 */
function extractContentMetadata(rankedItems: RankedItem[]): {
    contentIds: number[]
    contentTitles: string[]
} {
    const contentIds: number[] = []
    const contentTitles: string[] = []

    for (const item of rankedItems) {
        contentIds.push(item.content.id)
        const title = 'title' in item.content ? item.content.title : item.content.name
        if (title) {
            contentTitles.push(title.toLowerCase())
        }
    }

    return { contentIds, contentTitles }
}

/** Map a raw DB row to the Ranking domain type. */
function rowToRanking(row: typeof rankings.$inferSelect): Ranking {
    return {
        id: row.id,
        userId: row.userId,
        userName: row.userName ?? '',
        userAvatar: row.userAvatar ?? null,
        userUsername: row.userUsername ?? undefined,
        title: row.title,
        description: row.description ?? undefined,
        rankedItems: (row.rankedItems as RankedItem[]) ?? [],
        isPublic: row.isPublic,
        itemCount: row.itemCount,
        createdAt: row.createdAt,
        updatedAt: row.updatedAt,
        likes: row.likes,
        comments: row.comments,
        views: row.views,
        contentIds: (row.contentIds as number[]) ?? [],
        contentTitles: (row.contentTitles as string[]) ?? [],
        shareSettings: row.shareSettings ?? undefined,
        sharedLinkId: row.sharedLinkId ?? undefined,
    }
}

